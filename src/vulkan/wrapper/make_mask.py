import sys

def invert(mask):
    result = []
    items = [0] * 671
    for k,v in VK_IDS.items():
        items[v] = k
    for i in range(671):
        if mask & (1 << i) != 0:
            result.append(items[i])
    return result

def print_mask(mask):
    vk_functions = invert(mask)
    print(f"// full mask = {hex(mask)}")
    for i in range(16):
        local_mask = (mask >> (i * 64)) & ((1 << 64) - 1)
        if not local_mask: continue
        for function in vk_functions:
            idx = VK_IDS[function]
            if idx // 64 == i:
                print(f"// f{i} {"0x%016x" % (1 << (idx % 64))} = vk{function}")
        print("wrapper_printer_masks.f%d |= 0x%016xULL;" % (i, local_mask))

lines = sys.stdin if len(sys.argv) < 2 else open(sys.argv[1])
mask = 0

for line in lines:
    line = line.strip()
    if line.startswith("vk"):
        line = line[2:]
    if line.startswith("0x"):
        mask |= int(line, 16)
        continue
    if line not in VK_IDS:
        print(f"// '{line}' is not a valid Vulkan command")
        continue
    vkid = VK_IDS[line]
    mask |= (1 << vkid)

print_mask(mask)